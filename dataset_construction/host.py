class LLMBackend:
    """
    Backend that talks to a local OpenAI-compatible server (e.g., your vLLM at
    http://localhost:port/v1) and returns tool_calls parsed from the stream.
    """

    def __init__(
        self,
        model: str = "nvidia/Qwen3.6-35B-A3B-NVFP4",
        server_url: str | None = None,
        api_key: str | None = None,
        default_search_tool: bool = False,
    ):
        self.model = model
        self.server_url = server_url or os.getenv("DIRECT_BASE_URL", "http://localhost:port/v1")
        self.api_key = api_key or os.getenv("DIRECT_API_KEY", "EMPTY")
        self.default_search_tool = default_search_tool

        # Reuse a single client (and its underlying httpx connection pool)
        # across every chat() call instead of creating a brand-new one each
        # time, which previously leaked sockets/memory over long runs.
        self.client = AsyncOpenAI(base_url=self.server_url, api_key=self.api_key)

        self._fallback_tools = [
        ]

    async def aclose(self):
        """Close the underlying HTTP client/connection pool."""
        await self.client.close()
    
    def _count_tokens(self, msg: Dict[str, str], tools_schema) -> int:
        base = os.getenv("DIRECT_BASE_URL").rstrip("/")
        root = base[:-3].rstrip("/") if base.endswith("/v1") else base
        url = f"{root}/tokenize"

        payload = {
            "model": os.getenv("MODEL_NAME", "rdtand/Qwen3.6-35B-A3B-PrismaQuant-4.75bit-vllm"),
            "messages": msg,
            "add_generation_prompt": True,
            "tools": tools_schema,        # OpenAI-format tool schemas (list of {"type":"function","function":{...}})
            "chat_template_kwargs": {"enable_thinking": True},   # optional, if your template needs it
        }

        resp = requests.post(
            url,
            headers={"Authorization": f"Bearer {os.getenv('DIRECT_API_KEY')}"},
            json=payload,
        )
        resp.raise_for_status()
        return resp.json()["count"]
    
    def adjust_max_length(self, messages, max_tokens, tools_schema):
        """
        Adjust the max_length parameter for the model based on the token count of the messages and tools.

        Args:
            messages: List of message dictionaries
            max_tokens: Maximum allowed tokens

        Returns:
            Trimmed list of messages that fit within token limit
        """
        total = self._count_tokens(messages, tools_schema)

        # If already within limit, return as is
        if total <= max_tokens:
            return max_tokens - total
        else:
            return 0
    
    @retry(
        stop=stop_after_attempt(3),
        wait=wait_exponential(multiplier=1, min=2, max=10),
        reraise=True,
        before_sleep=before_sleep_log(logger, logging.WARNING),
    )
    async def chat(
        self,
        messages: List[Dict[str, Any]],
        tools: List[Dict[str, Any]] | None = None,
        tool_choice: str | None = "auto",
        max_tokens: int = 65536,
    ) -> Dict[str, Any]:

        client = self.client
        max_tokens = self.adjust_max_length(messages=messages, max_tokens=max_tokens, tools_schema=tools)
        if max_tokens == 0:
            return {"content": None, "reasoning": None, "tool_calls": None, "finish_reason": "context_length_exceeded"}
        payload: Dict[str, Any] = {
            "model": self.model,
            "messages": messages,
            "max_tokens": max_tokens,
            "stream": True,
            "temperature": 1.0,
            "top_p": 0.95,
            "presence_penalty": 1.5,
            "extra_body": {
                "top_k": 20,
                "min_p": 0.0,
                "repetition_penalty": 1.0,
                "chat_template_kwargs": {"preserve_thinking": True}
            }
        }

        if tools and len(tools) > 0:
            payload["tools"] = tools
        elif self.default_search_tool:
            payload["tools"] = self._fallback_tools

        payload["tool_choice"] = tool_choice or "auto"

        stream = await client.chat.completions.create(**payload)

        aggregated_content = ""
        aggregated_reasoning = ""
        tool_call_buffers: Dict[int, Dict[str, Any]] = {}
        finish_reason = None
        
        async for chunk in stream:
            choice = chunk.choices[0]
            delta = choice.delta

            if getattr(choice, "finish_reason", None):
                finish_reason = choice.finish_reason

            if getattr(delta, "content", None): # content outside <thknk> block
                aggregated_content += delta.content
                
            if getattr(delta, "reasoning", None): # corresponds to thinking process
                aggregated_reasoning += delta.reasoning
                
            if getattr(delta, "tool_calls", None):
                for tc in delta.tool_calls:
                    idx = getattr(tc, "index", 0) or 0
                    b = tool_call_buffers.setdefault(
                        idx,
                        {"id": None, "type": "function", "function": {"name": "", "arguments": ""}},
                    )
                    if getattr(tc, "id", None):
                        b["id"] = tc.id
                    func = getattr(tc, "function", None)
                    if func is not None:
                        if getattr(func, "name", None):
                            b["function"]["name"] = func.name
                        if getattr(func, "arguments", None):
                            b["function"]["arguments"] += func.arguments

        tool_calls = list(tool_call_buffers.values()) if tool_call_buffers else None
        content = aggregated_content.strip() if aggregated_content else None
        reasoning = aggregated_reasoning.strip() if aggregated_reasoning else None
        return {"content": content, "reasoning": reasoning, "tool_calls": tool_calls, "finish_reason": finish_reason}
    
