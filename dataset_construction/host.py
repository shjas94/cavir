def _base64_to_pil_and_save(b64_string: str, save_path: str) -> Image.Image:
    """
    Convert a base64-encoded image string to a PIL Image and save it to disk.
    Args:
        b64_string: Base64-encoded image string (with or without data URI prefix)
        save_path: Destination file path to save the image
    Returns:
        The decoded PIL Image
    """
    # Strip optional data URI prefix (e.g. "data:image/png;base64,")
    if "," in b64_string:
        b64_string = b64_string.split(",", 1)[1]
    b64_string = b64_string.strip()
    image_bytes = base64.b64decode(b64_string)
    pil_image = Image.open(BytesIO(image_bytes))
    Path(save_path).parent.mkdir(parents=True, exist_ok=True)
    pil_image.save(save_path)
    return pil_image


def _sanitize_tool_call_arguments(raw_arguments: str) -> str:
    """
    Strip any raw image payload the model may have hallucinated into a tool
    call's arguments before it is persisted into conversation history, so a
    single bad generation doesn't bloat every subsequent turn's context.
    """
    if not raw_arguments:
        return raw_arguments
    try:
        parsed = ast.literal_eval(raw_arguments)
    except (SyntaxError, ValueError):
        return raw_arguments
    if not isinstance(parsed, dict):
        return raw_arguments
    stripped = False
    for key in ("image", "image_base64", "img", "img_b64", "image_data"):
        if key in parsed:
            del parsed[key]
            stripped = True
    if not stripped:
        return raw_arguments
    return json.dumps(parsed)

class CaViRAgent:
    """
    Main client class that orchestrates the Agent system.

    Handles tool connections, and processes user queries through
    multiple generations and tool calling cycles.
    """

    # Maximum number of planning cycles before giving up
    MAX_CYCLES = 3

    def __init__(self):
        """
        Initialize the CaViRAgent.
        """
        self.llm_agent = LLMBackend(model=os.getenv("MODEL_NAME", "rdtand/Qwen3.6-35B-A3B-PrismaQuant-4.75bit-vllm"), server_url=os.getenv("DIRECT_BASE_URL"), api_key=os.getenv("DIRECT_API_KEY"))
        
        self.sessions: Dict[str, ClientSession] = {}

    def _resolve_tool_name(self, requested: str) -> str:
        if requested in self.sessions:
            return requested

        aliases = {
            "search": ["serp_search"],
        }
        for cand in aliases.get(requested, []):
            if cand in self.sessions:
                return cand

        for name in self.sessions.keys():
            if requested in name or name in requested:
                return name

        raise KeyError(f"No matching tool for '{requested}'. Available: {list(self.sessions.keys())}")

    def _message_args_for_tool(self, tool_name: str, args: Dict[str, Any]) -> Dict[str, Any]:
        patched = dict(args)
        ln = tool_name.lower()
        return patched

    # ---------- Tool management ----------
    async def connect_to_servers(self, scripts: List[str]):
        """
        Connect to MCP tool servers specified by script paths.

        Args:
            scripts: List of paths to tool server scripts

        Raises:
            RuntimeError: If duplicate tool names are found
        """
        from contextlib import AsyncExitStack
        self.exit_stack = AsyncExitStack()

        for script in scripts:
            path = Path(script)
            # Determine command based on file extension
            cmd = "python"
            params = StdioServerParameters(command=cmd, args=[str(path)])

            # Create stdio client and session
            stdio, write = await self.exit_stack.enter_async_context(stdio_client(params))
            session = await self.exit_stack.enter_async_context(ClientSession(stdio, write))
            await session.initialize()

            # Register tools from this session
            for tool in (await session.list_tools()).tools:
                if tool.name in self.sessions:
                    raise RuntimeError(f"Duplicate tool name '{tool.name}'.")
                self.sessions[tool.name] = session

        print("Connected tools:", list(self.sessions.keys()))

    async def _tools_schema(self) -> List[Dict[str, Any]]:
        """
        Get the schema for all available tools in a format suitable for OpenAI.

        Fields that the host injects itself (raw "image" bytes, model_name,
        api_key, host_url) are stripped from the schema so the model can never
        be asked/tempted to generate them. Tools that need an image instead
        get a synthetic "image_index" integer parameter (see IMAGE_INPUT_TOOLS
        / IMAGE_PRODUCING_TOOLS) that the host resolves against the per-task
        image history before invoking the tool.

        Returns:
            List of tool schemas in OpenAI function calling format
        """
        result, cached = [], {}
        for session in self.sessions.values():
            # Cache tool listings to avoid repeated calls
            tools_resp = cached.get(id(session)) or await session.list_tools()
            cached[id(session)] = tools_resp

            # Convert MCP tool format to OpenAI function calling format
            for tool in tools_resp.tools:
                schema = copy.deepcopy(tool.inputSchema) or {}
                properties = schema.get("properties", {})
                required = schema.get("required", [])

                for field in HOST_INJECTED_FIELDS:
                    properties.pop(field, None)
                    if field in required:
                        required.remove(field)

                if tool.name in IMAGE_INPUT_TOOLS:
                    properties["image_index"] = {
                        "type": "integer",
                        "description": (
                            "Index of the image to operate on, NOT the raw image data. "
                            "0 is the original input image. Each successful call to "
                            "crop_zoomin, mark_dots, or draw_bbox appends a new image "
                            "to the history (1 = first such image, 2 = second, etc.); "
                            "use that index to operate on a previously produced image "
                            "instead of the original."
                        ),
                        "default": 0,
                    }

                schema["properties"] = properties
                schema["required"] = required

                result.append(
                    {
                        "type": "function",
                        "function": {
                            "name": tool.name,
                            "description": tool.description,
                            "parameters": schema,
                        },
                    }
                )
        return result

    # ---------- Main processing ----------
    async def process_query(self, args, query: str, img_b64: str, image_path: str, task_id: str, gt: str, semaphore: asyncio.Semaphore):
        """
        Args:
            query: User's question or request
            img_b64: Optional base64 string of an image to include in the query
            image_path: Optional path to an image file to include in the query
            task_id: Unique identifier for this query session
            gt: Ground truth answer for evaluation purposes
            semaphore: Asyncio semaphore for controlling concurrency
        """
        
        async with semaphore:
            tools_schema = await self._tools_schema()
            tool_result_dir = f"{args.output_dir}/tool_results/{task_id}"
            os.makedirs(tool_result_dir, exist_ok=True)            
            llm_agent_message = [
                {
                    "role": "system",
                    "content": GENERATION_PROMPT
                },
                {
                    "role": "user",
                    "content": [
                        {
                            "type": "text",
                            "text": f"QUESTION:\n{query}\nINPUT_IMAGE:\n"
                        },
                        {
                            "type": "image_url",
                            "image_url": {
                                "url": f"data:image/png;base64,{img_b64}"
                            }
                        }
                    ]
                }
            ]
            result_message = [
                {   
                    "role": "user",
                    "idx": 0,
                    "content": {
                        "question": query,
                        "input_image": image_path,
                        "gt": gt
                    }
                }
            ]
            result_idx = 1
            # Per-task image history: index 0 is always the original input
            # image. Every successful crop_zoomin/mark_dots/draw_bbox call
            # appends its resulting image here, and the model references
            # images by index (`image_index`) instead of raw base64 bytes.
            image_history: List[str] = [img_b64]

            def append_result(entry: Dict[str, Any]):
                nonlocal result_idx
                entry["idx"] = result_idx
                result_message.append(entry)
                result_idx += 1

            def sanitize_args(call_args: Dict[str, Any], image_index: Any = None) -> Dict[str, Any]:
                sanitized = dict(call_args)
                if "image" in sanitized:
                    sanitized["image"] = f"<image_index={image_index}>" if image_index is not None else "<image data omitted>"
                return sanitized

            # Main generating and tool-calling loop
            tool_called = 0
            while True:
                llm_agent_reply = await self.llm_agent.chat(llm_agent_message, tools_schema)
                llm_content = llm_agent_reply["content"]
                llm_reasoning = llm_agent_reply["reasoning"]
                finish_reason = llm_agent_reply["finish_reason"]
                tool_calls = llm_agent_reply["tool_calls"]

                # Assign a stable id to each tool call so that the assistant
                # message and its matching tool messages share the same id.
                if tool_calls:
                    for call in tool_calls:
                        if not call.get("id"):
                            call["id"] = str(uuid.uuid4())
                        # Strip any hallucinated raw image data from the arguments
                        # before they get persisted into conversation history.
                        call["function"]["arguments"] = _sanitize_tool_call_arguments(
                            call["function"].get("arguments", "")
                        )

                # Reconstruct a SINGLE assistant message for this turn. One model
                # generation = reasoning_content + content + all tool_calls.
                # Splitting these into separate assistant messages breaks the
                # chat template and the reasoning<->tool-call association.
                assistant_msg: Dict[str, Any] = {"role": "assistant"}
                if llm_reasoning:
                    # vLLM's OpenAI-compatible chat template expects
                    # "reasoning_content" (not "reasoning") on input messages.
                    assistant_msg["reasoning"] = llm_reasoning
                assistant_msg["content"] = llm_content or ""
                if tool_calls:
                    assistant_msg["tool_calls"] = tool_calls
                llm_agent_message.append(assistant_msg)

                # for text inside <think>. corresponds to reasoning process
                if llm_reasoning:
                    append_result({
                        "role": "assistant",
                        "content": {
                            "llm_reasoning": llm_reasoning,
                            "finish_reason": finish_reason
                        }
                    })

                if tool_calls:
                    # Every tool_call in the assistant message MUST be followed by
                    # a matching tool message, otherwise the server rejects the
                    # request. So we always emit a tool response per call, and only
                    # flag whether the loop should stop afterwards.
                    stop_generation = False
                    for call in tool_calls:
                        call_id = call["id"]
                        tool_called += 1
                        if tool_called > MAX_TOOL_CALLS:
                            tool_result = "Exceeded maximum tool calls limit."
                            finish_reason = "Tool call limit exceeded"
                            llm_agent_message.append(
                                {"role": "tool", "tool_call_id": call_id, "name": "None", "content": tool_result}
                            )
                            append_result({
                                "role": "assistant",
                                "content": {
                                    "tool_call": [call],
                                    "tool_call_id": call_id,
                                    "tool_name": "None",
                                    "tool_call_args": "None",
                                    "tool_call_result": tool_result,
                                    "finish_reason": finish_reason
                                }
                            })
                            stop_generation = True
                            continue
                        t_name = call["function"]["name"]
                        try:
                            t_args = ast.literal_eval(call["function"].get("arguments", "{}")) # validate json
                        except (SyntaxError, ValueError) as e:
                            error_msg = f"[tool argument parse error] {e}: {call['function'].get('arguments', '')}"
                            finish_reason = "error"
                            llm_agent_message.append(
                                {"role": "tool", "tool_call_id": call_id, "name": t_name, "content": error_msg}
                            )
                            append_result({
                                "role": "assistant",
                                "content": {
                                    "tool_call": [call],
                                    "tool_call_id": call_id,
                                    "tool_name": t_name,
                                    "tool_call_args": call["function"].get("arguments", ""),
                                    "tool_call_result": error_msg,
                                    "finish_reason": finish_reason
                                }
                            })
                            # Malformed tool-call arguments -> finish the loop and generate next sample
                            stop_generation = True
                            continue
                        # Resolve `image_index` (0 = original input image, N = the
                        # Nth image produced by a previous crop_zoomin/mark_dots/
                        # draw_bbox call) to the actual base64 bytes the tool needs.
                        used_image_index = None
                        if t_name in IMAGE_INPUT_TOOLS:
                            requested_index = t_args.pop("image_index", 0)
                            try:
                                requested_index = int(requested_index)
                            except (TypeError, ValueError):
                                requested_index = 0
                            if not (0 <= requested_index < len(image_history)):
                                requested_index = 0
                            used_image_index = requested_index
                            t_args["image"] = image_history[requested_index]
                            if t_name in ['crop_ocr_image', 'imaginate_editing']:
                                t_args['model_name'] = os.getenv("MODEL_NAME", "rdtand/Qwen3.6-35B-A3B-PrismaQuant-4.75bit-vllm")
                                t_args['api_key'] = os.getenv("DIRECT_API_KEY", "")
                                t_args['host_url'] = os.getenv("DIRECT_BASE_URL", "")
                        try:
                            resolved = self._resolve_tool_name(t_name)
                        except KeyError as e:
                            error_msg = f"[tool resolution error] {e}"
                            finish_reason = "error"
                            llm_agent_message.append(
                                {"role": "tool", "tool_call_id": call_id, "name": t_name, "content": error_msg}
                            )
                            append_result({
                                "role": "assistant",
                                "content": {
                                    "tool_call": [call],
                                    "tool_call_id": call_id,
                                    "tool_name": t_name,
                                    "tool_call_args": str(sanitize_args(t_args, used_image_index)),
                                    "tool_call_result": error_msg,
                                    "finish_reason": finish_reason
                                }
                            })
                            # Tool call error -> finish the loop and generate next sample
                            stop_generation = True
                            continue
                        session = self.sessions[resolved]
                        patched_args = self._message_args_for_tool(resolved, t_args)

                        result_msg = await session.call_tool(resolved, patched_args)
                        # save tool calling results to output dir if the tool is image processing
                        save_path = None
                        tool_error = getattr(result_msg, "isError", False)
                        if t_name in ['crop_zoomin', 'mark_dots', 'draw_bbox']:
                            first = result_msg.content[0]
                            if tool_error or not hasattr(first, "data"):
                                # Tool failed internally; first is a TextContent error message
                                tool_result = getattr(first, "text", "Unknown image tool error")
                                tool_error = True
                            else:
                                tool_result = first.data  # ImageContent.data is base64 string
                                save_path = f"{tool_result_dir}/{t_name}_{call_id}.png"
                                _base64_to_pil_and_save(tool_result, save_path)
                                # Grow the per-task image history so later tool calls
                                # can reference this newly produced image by index.
                                image_history.append(tool_result)
                        elif t_name in ['crop_ocr_image', 'imaginate_editing']:
                            tool_result = result_msg.content[0].text
                        else:  # code_execution_tool
                            tool_result = result_msg.content[0].text
                            try:
                                tool_result = ast.literal_eval(tool_result).get("execution_result", "")
                            except:
                                tool_result = f"Error parsing tool result: {tool_result}"
                                tool_error = True

                        tool_finish_reason = "tool result error" if tool_error else None

                        if t_name in IMAGE_PRODUCING_TOOLS and not tool_error:
                            # Tell the model which image_index it can now use to
                            # refer back to the image this call just produced.
                            new_image_index = len(image_history) - 1
                            tool_message_content = f"[image_index={new_image_index}]\n{tool_result}"
                        else:
                            tool_message_content = tool_result

                        llm_agent_message.append(
                            {"role": "tool", "tool_call_id": call_id, "name": resolved, "content": tool_message_content}
                        )
                        append_result({
                            "role": "assistant",
                            "content": {
                                "tool_call": [call],
                                "tool_call_id": call_id,
                                "tool_name": resolved,
                                "tool_call_args": str(sanitize_args(patched_args, used_image_index)),
                                "tool_call_result": save_path if (t_name in ['crop_zoomin', 'mark_dots', 'draw_bbox'] and save_path) else tool_result,
                                "finish_reason": tool_finish_reason
                            }
                        })

                # for text after </think>. corresponds to final answer
                if llm_content:
                    append_result({
                        "role": "assistant",
                        "content": {
                            "llm_content": llm_content,
                            "finish_reason": finish_reason
                        }
                    })

                if tool_calls and stop_generation:
                    break

                if finish_reason is not None and finish_reason not in ["tool_calls", "Tool call limit exceeded"]:
                    break
            async with aiofiles.open(os.path.join(args.output_dir, args.output_json), "a") as f:
                await f.write(json.dumps({
                    "id": task_id,
                    "query": query,
                    "image_path": image_path,
                    "gt": gt,
                    "result": result_message,
                    "tool_results_dir": tool_result_dir,
                    "finish_reason": finish_reason
                }, default=str) + "\n")
                await f.flush()
            

    async def cleanup(self):
        """Clean up resources and close tool server connections."""
        if hasattr(self, "exit_stack"):
            await self.exit_stack.aclose()
        await self.llm_agent.aclose()



def parse_args():
    parser = argparse.ArgumentParser(description="CaViR Dataset Construction Host")
    parser.add_argument("--dataset-name", type=str, required=True, help="Name of the dataset to construct")
    parser.add_argument("--concurrency", type=int, default=64, help="Number of concurrent queries to process")
    parser.add_argument("--servers", type=str, nargs="*", default=[
        "/workspace/cavir/dataset_construction_v2/server/code_execution_tool.py",
        "/workspace/cavir/dataset_construction_v2/server/image_processing_tool.py",
    ], help="Paths of tool server scripts")
    parser.add_argument("--output-json", type=str, required=True, help="Path to output JSON file for the constructed dataset")
    parser.add_argument("--output-dir", type=str, required=True, help="Directory to save intermediate tool call results and reasoning traces")
    parser.add_argument("--sample", type=int, default=-1, help="Number of samples to process (default: -1 (all))")
    return parser.parse_args()

async def main_async(args):
    load_dotenv()
    dataset = HFDataset(args.dataset_name, DATASET_PATHS[args.dataset_name])
    agent = CaViRAgent()
    semaphore = asyncio.Semaphore(args.concurrency)
    await agent.connect_to_servers(args.servers)
    loader = DataLoader(
        dataset,
        batch_size=1,
        shuffle=False,
        num_workers=0,
        collate_fn=collate_fn
    )
    os.makedirs(args.output_dir, exist_ok=True)

    pending: set[asyncio.Task] = set()
    try:
        total_samples = len(loader) if args.sample == -1 else min(len(loader), args.sample)
        with tqdm(total=total_samples, desc="Processing dataset") as pbar:
            for i, batch in enumerate(loader):
                if args.sample != -1 and i >= args.sample:
                    break
                item = batch[0]
                if len(pending) >= args.concurrency:
                    done, pending = await asyncio.wait(
                        pending, return_when=asyncio.FIRST_COMPLETED
                    )
                    for d in done:
                        d.result()
                        pbar.update(1)
                pending.add(asyncio.create_task(agent.process_query(
                    args,
                    item['question'],
                    item['image'],
                    item['image_path'],
                    item['id'],
                    item['gt'],
                    semaphore,
                )))
            while pending:
                done, pending = await asyncio.wait(
                    pending, return_when=asyncio.FIRST_COMPLETED
                )
                for d in done:
                    d.result()
                    pbar.update(1)
    finally:
        await agent.cleanup()


if __name__ == "__main__":
    args = parse_args()
    asyncio.run(main_async(args=args))